for i in range(len(entry_count_top3)):
    print(f'{entry_count_top3[i][0]} {entry_count_top3[i][1]}회')


print('출입 기록이 수상한 사람')

unmatch_name = []

for name in entry_count.keys():
    if entry_count.get(name) != exit_count.get(name):
        unmatch_name.append(name)

